#include "libft.h"

int		ft_strnequ(const char *s1, const char *s2, size_t n)
{
	if (!s1 || !s2)
		return (0);
	while (*s1 == *s2)
	{
		n--;
		if (*s1 == '\0')
			return (1);
		s1++;
		s2++;
	}
	if (!n || !*s1 || !*s2)
		return (1);
	return (0);
}

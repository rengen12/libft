#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
	{
		ft_putnbr(-214748364, fd);
		ft_putnbr(8, fd);
	}
	if (n < 0)
	{
		ft_putchar('-', fd);
		n *= -1;
	}
	if (n >= 10)
		ft_putnbr(n / 10, fd);
	ft_putchar(n % 10 + '0');
}
